package com.freecodecamp.Operators;

public class Operators {
    public static void main(String[] args) {
        int num1 = 20;
        int num2 = 10;

        // addition
        System.out.println("The addition is: " + (num1 + num2));

        // subtraction
        System.out.println("The subtraction is: " + (num1 - num2));

        // multiplication
        System.out.println("The multiplication is: " + (num1 * num2));

        // division
        System.out.println("The division is: " + (num1 / num2));

        System.out.println("------------------------------------------");

        // modulo
        System.out.println("The module is: " + (num1 % num2));

        System.out.println("------------------------------------------");

        // logic operators
        // equal
        System.out.println("The number is equal: " + (num1 == num2));

        // not equal
        System.out.println("The number is not equal: " + (num1 != num2));

        // greater than
        System.out.println("The number 1 is greater than number 2: " + (num1 > num2));

        // less than
        System.out.println("The number 1 is less than number 2: " + (num1 < num2));

        // less than or equal
        System.out.println("The number 1 is greater or equal to number 2: " + (num1 >= num2));

        // less than or equal
        System.out.println("The number 1 is less or equal to number 2: " + (num1 <= num2));
    }
}
